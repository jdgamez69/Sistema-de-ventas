    private void btn_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IngresarActionPerformed
//        this.Login();
        Registro registro = new Registro();
        Registrar frmRegistrar = new Registrar();
        Ctrl_Registro consultar_usuario = new Ctrl_Registro();
        Ctrl_Administrador admin = new Ctrl_Administrador();
        String usuario,clave;
        usuario = this.txt_Usuario.getText();
        clave = this.password.getText();
        Ctrl_Usuario ctrl = new Ctrl_Usuario(registro, consultar_usuario, frmRegistrar, admin);
        if(ctrl.Login(usuario,clave)){
            this.txt_Usuario.setText("");
            this.password.setText("");
            Menu menu = new Menu();
            menu.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_IngresarActionPerformed

